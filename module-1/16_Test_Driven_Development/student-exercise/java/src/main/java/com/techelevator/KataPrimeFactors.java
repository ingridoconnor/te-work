package com.techelevator;

import java.util.ArrayList;
import java.util.List;

public class KataPrimeFactors {
	public List<Integer>  factorize(int n) {
		
		List<Integer> primeList = new ArrayList<>();
		
		
		
		if(n < 2) {
			return primeList;
		
		}
		if(n < 4) {
			return primeList;
		}
		return primeList;
		
	}

}
